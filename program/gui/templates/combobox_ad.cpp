#include "combobox_ad.h"

#include "application.h"
#include "business_logic.h"
#include "data_model_ad.h"
#include "data_adapter_ad.h"

namespace mac_mediator
{

/// ############################################################################
///			class combobox_ad
/// ############################################################################

    /// ========================================================================
    ///		CONSTRUCTORS/DESTRUCTOR
    /// ========================================================================

    /// ------------------------------------------------------------------------
	///	combobox_ad( )
    /// ------------------------------------------------------------------------
    combobox_ad::combobox_ad(QWidget *parent) :
        QComboBox(parent)
    {
        this->initialize( );
    }
    /// ------------------------------------------------------------------------
    ///	~combobox_ad( )
    /// ------------------------------------------------------------------------
    combobox_ad::~combobox_ad( )
    {

    }
    /// ========================================================================
    ///		PROPERTIES
    /// ========================================================================
    /// ------------------------------------------------------------------------
    ///	id_ad( )
    /// ------------------------------------------------------------------------
    int combobox_ad::id_ad( )
    {
        if( this->currentIndex( ) == -1 )
        {
            return -1;
        }
        return this->itemData( this->currentIndex( ) ).toInt( );
    }

    /// ========================================================================
    ///		FUNCTIONS
    /// ========================================================================

    /// ------------------------------------------------------------------------
	///	initialize( )
    /// ------------------------------------------------------------------------
    void combobox_ad::initialize( )
    {
        const data_ad_list& list =
                    application::the_business_logic().model_ad( )->list( );
        if( !list.size() )
        {
            return;
        }

        this->addItem( "", -1 );
        for( int i=0; i<list.size();++i)
        {
            const data_ad *ad = list.at(i);
            QString strItem = QString::number( ad->ad_id( ) ) +"_" + ad->ad_title( );
            this->addItem( strItem, ad->ad_id( ) );
        }
    }

    /// ------------------------------------------------------------------------
	///	set_id_ad( )
    /// ------------------------------------------------------------------------
    void combobox_ad::set_id_ad( const int id_ad )
    {
        int index = this->findData( id_ad );
        if( index >= 0 )
        {
            this->setCurrentIndex( index );
        }
    }

}//namespace mac_mediator
