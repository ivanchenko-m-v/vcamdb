#include <QHBoxLayout>

#include "horizontal_box.h"

namespace espira
{

namespace widgets
{

/// ############################################################################
///			class horizontal_box
/// ############################################################################

    /// ========================================================================
    ///		CONSTRUCTORS/DESTRUCTOR
    /// ========================================================================

    /// ------------------------------------------------------------------------
    ///	horizontal_box( )
    /// ------------------------------------------------------------------------
    horizontal_box::horizontal_box( QWidget *w, const QString &x_lbl, QWidget *parent) :
        QWidget( parent ),
        _widget( w ),
        _x_lbl( x_lbl )
    {
        this->initialize( );
    }
    /// ------------------------------------------------------------------------
    ///	~horizontal_box( )
    /// ------------------------------------------------------------------------
    horizontal_box::~horizontal_box( )
    {

    }

    /// ========================================================================
    ///		FUNCTIONS
    /// ========================================================================
    /// ------------------------------------------------------------------------
    /// initialize( )
    /// ------------------------------------------------------------------------
    void horizontal_box::initialize( )
    {
        this->init_layout( );
    }

    /// ------------------------------------------------------------------------
    /// init_layout( )
    /// ------------------------------------------------------------------------
    void horizontal_box::init_layout( )
    {
        QHBoxLayout *layout = new QHBoxLayout;

        if( this->_widget == 0 )
        {
            this->_widget = new QWidget( this->parentWidget( ) );
        }
        layout->addWidget( this->_widget, _MAX_STRETCH_  );
        layout->addWidget( new QLabel( this->_x_lbl,this ), _MIN_STRETCH_ );

        this->setLayout( layout);
    }

    /// ========================================================================
    ///		EVENTS
    /// ========================================================================

    /// ========================================================================
    ///		SLOTS
    /// ========================================================================

}//namespace widgets

}//namespace espira

