#include <QApplication>
#include <QDesktopWidget>

#include "application.h"

#include "main_window.h"
#include "widget_central.h"

namespace vcamdb
{

/// ############################################################################
///			class main_window
/// ############################################################################

    /// ========================================================================
    ///		CONSTRUCTORS/DESTRUCTOR
    /// ========================================================================

	///-------------------------------------------------------------------------
	///	main_window( )
    ///-------------------------------------------------------------------------
    main_window::main_window( QWidget *parent ) :
		QMainWindow( parent )
	{
		this->initialize( );
	}


    ///-------------------------------------------------------------------------
    ///	~main_window( )
    ///-------------------------------------------------------------------------
    main_window::~main_window( )
	{
	}

    /// ========================================================================
    ///		PROPERTIES
    /// ========================================================================

    /// ========================================================================
    ///		OPERATORS
    /// ========================================================================

    /// ========================================================================
    ///		FUNCTIONS
    /// ========================================================================
    ///-------------------------------------------------------------------------
    ///	initialize( )
    ///-------------------------------------------------------------------------
    void main_window::initialize( )
	{
        QString s_title( tr("ogh videocams application") );
        s_title += "-[" + application::program_instance()->user( ) + "]";
        this->setWindowTitle( s_title );

		this->setMinimumSize( 940, 705 );
		this->window_center( );
		//_workspace
        this->_workspace = new widget_central( this );
		this->setCentralWidget( this->_workspace );
	}

	///------------------------------------------------------------------------
	///	window_center( )
    ///-------------------------------------------------------------------------
    void main_window::window_center( )
	{
		QDesktopWidget *desktop = QApplication::desktop( );
		this->move(
					( desktop->geometry( ).width( ) -
						this->frameGeometry( ).width( ) )/2,
					( desktop->geometry( ).height( ) -
						this->frameGeometry( ).height( ) )/2
				  );
	}

    ///------------------------------------------------------------------------
    ///	append_title_text( )
    ///-------------------------------------------------------------------------
    void main_window::append_title_text( const QString &text )
    {
        QString wtitle( this->windowTitle( ) + "-[" + text + "]" );
        this->setWindowTitle( wtitle );
    }

    /// ========================================================================
    ///		SLOTS
    /// ========================================================================

/// ############################################################################

}//namespace vcamdb
