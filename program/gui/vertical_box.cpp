#include <QVBoxLayout>
#include "vertical_box.h"

namespace espira
{

namespace widgets
{

/// ############################################################################
///			class vertical_box
/// ############################################################################

    /// ========================================================================
    ///		CONSTRUCTORS/DESTRUCTOR
    /// ========================================================================

    /// ------------------------------------------------------------------------
    ///	vertical_box( )
    /// ------------------------------------------------------------------------
    vertical_box::vertical_box( QWidget *w, const QString &x_lbl, QWidget *parent) :
        QWidget( parent ),
        _widget( w ),
        _x_lbl( x_lbl )
    {
        this->initialize( );
    }
    /// ------------------------------------------------------------------------
    ///	~vertical_box( )
    /// ------------------------------------------------------------------------
    vertical_box::~vertical_box( )
    {

    }

    /// ========================================================================
    ///		FUNCTIONS
    /// ========================================================================
    /// ------------------------------------------------------------------------
    /// initialize( )
    /// ------------------------------------------------------------------------
    void vertical_box::initialize( )
    {
        this->init_layout( );
    }

    /// ------------------------------------------------------------------------
    /// init_layout( )
    /// ------------------------------------------------------------------------
    void vertical_box::init_layout( )
    {
        QVBoxLayout *layout = new QVBoxLayout;
        layout->setMargin( 0 );
        layout->setSpacing( 0 );

        layout->addWidget( new QLabel( this->_x_lbl, this ) );
        if( this->_widget == 0 )
        {
            this->_widget = new QWidget( this->parentWidget( ) );
        }
        layout->addWidget( this->_widget );
        layout->addStretch( );

        this->setLayout( layout);
    }

    /// ========================================================================
    ///		EVENTS
    /// ========================================================================

    /// ========================================================================
    ///		SLOTS
    /// ========================================================================

}//namespace widgets

}//namespace espira

